#################################################################################
## SGmisc Package-building sandbox
#################################################################################

library(devtools)
library(roxygen2)
packpath <- file.path(Sys.getenv("DEVEL"), "SGmisc")
setwd(packpath)
getwd()

#################################################################################
## Always (re)generate documentation, and then check package from source tree
#################################################################################
devtools::document()
devtools::check()

#################################################################################
## Install SGmisc from the source tree
#################################################################################
install.packages(packpath, repos = NULL, type = "source")

